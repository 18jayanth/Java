public class Java_4_literals {
    public static void main(String[] args) {
        byte a=15;
        short b=15000;
        int  c=1500000000;
        long d=150000000000000L;
        float e=120.01234f;
        double f=150000.0340449848934;
        char g='A';
        boolean h=true;
        String i="Jayanth";
        System.out.println(a+" "+b+" "+c+" "+d);
        System.out.println(e+" "+f);
        System.out.print(g+" "+h+" "+i);
//        15 15000 1500000000 150000000000000
//        120.01234 150000.0340449849
//        A true Jayanth

    }
}
